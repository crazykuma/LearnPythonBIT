import numpy as np
# 思路1
sum = 0
n = 966
for i in range(1, n+1):
    if i % 2 == 0:
        sum += i
    else:
        sum -= i

print(sum)

# 思路2

z = [i*(-1)*(-1)**i for i in range(1, n+1)]
print(np.sum(z))
